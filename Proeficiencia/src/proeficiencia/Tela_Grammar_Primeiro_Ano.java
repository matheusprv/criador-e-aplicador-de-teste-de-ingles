    private void pegar_questoes_bd() throws SQLException {
        Connection bd = Tela_Inicial.getConnection();
        int cont=0;
        String query;
        
        query = "SELECT * FROM tb_grammar_primeiro_ano";
        PreparedStatement preparedStmt = bd.prepareStatement(query);
        preparedStmt = bd.prepareStatement(query);
        ResultSet rs = preparedStmt.executeQuery();
        while(rs.next()){ 
            if(cont<11){
                questoes[cont]=rs.getString("questoes_respostas");
                cont++;
            }
        }
        rs.close();
	preparedStmt.close();
        Tela_Inicial.closeConnection(bd);
        arrumar_questoes();
    }
