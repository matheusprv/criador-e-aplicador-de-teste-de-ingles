    private void verificar_listening(){
        
        File arquivo = new File("Terminou_o_listening.txt");
        try{
            FileReader ler = new FileReader(arquivo);
            BufferedReader ler_arquivo= new BufferedReader(ler);
            terminar[0]=ler_arquivo.readLine();
            
        }catch(IOException ex){
            
        }         
        verificar_reading();
    }
    private void verificar_reading(){

        File arquivo = new File("Terminou_o_reading.txt");
        try{
            FileReader ler = new FileReader(arquivo);
            BufferedReader ler_arquivo= new BufferedReader(ler);
            terminar[1]=ler_arquivo.readLine();
            
        }catch(IOException ex){
            
        }    
        verificar_grammar();
    }
    private void verificar_grammar(){

        File arquivo = new File("Terminou_o_grammar.txt");
        try{
            FileReader ler = new FileReader(arquivo);
            BufferedReader ler_arquivo= new BufferedReader(ler);
            terminar[2]=ler_arquivo.readLine();
            
        }catch(IOException ex){
            
        }         
        exibir_mensagem();
    }
    private void exibir_mensagem(){
        if(terminar [0].equals("nao") & terminar [1].equals("nao") & terminar [2].equals("nao") ){
            JOptionPane.showMessageDialog(this, "Tenha atenção ao terminar um campo, pois não será possível refazê-lo.");
        }
    }
