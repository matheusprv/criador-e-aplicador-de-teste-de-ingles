    private void botao_finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_finalizarActionPerformed
        if(finalizar==0){
            JOptionPane.showMessageDialog(this,"Clique novamente no botão 'finalizar' para ver o resultado. NÃO será possível fazer a prova novamente","Alerta",JOptionPane.WARNING_MESSAGE); 
        }
        else{
            Tela_Finalizar a = new Tela_Finalizar();
            a.setVisible(true);
            this.setVisible(false);            
        }
        finalizar++;

    }//GEN-LAST:event_botao_finalizarActionPerformed

